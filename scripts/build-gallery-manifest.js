#!/usr/bin/env node
/**
 * Generates src/config/galleryManifest.js from files in:
 *   public/gallery/optimized/  (preferred — uses *.webp full + *.thumb.webp)
 *   public/gallery/videos/
 *
 * Falls back to public/gallery/images/ if no optimized assets exist.
 *
 * Run with:  npm run gallery:manifest
 * Drop new originals into public/gallery/images/ then run:
 *   npm run gallery:optimize  (creates webp variants)
 *   npm run gallery:manifest  (rebuilds manifest)
 * Or simply `npm run gallery:build` for both.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');

const enc = (urlPath) =>
    urlPath.split('/').map((seg) => encodeURIComponent(seg)).join('/');

const list = (dir, regex) => {
    const abs = path.join(root, 'public', dir);
    if (!fs.existsSync(abs)) return [];
    return fs.readdirSync(abs)
        .filter((f) => regex.test(f))
        .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));
};

const optDir = 'gallery/optimized';
const optFiles = list(optDir, /\.webp$/i);
const fullFiles = optFiles
    .filter((f) => !/\.thumb\.webp$/i.test(f));

let images;
if (fullFiles.length > 0) {
    const optSet = new Set(optFiles);
    images = fullFiles.map((f) => {
        const base = f.replace(/\.webp$/i, '');
        const thumbName = `${base}.thumb.webp`;
        const thumbExists = optSet.has(thumbName);
        return {
            src: '/' + enc(`${optDir}/${f}`),
            thumb: '/' + enc(`${optDir}/${thumbExists ? thumbName : f}`),
        };
    });
} else {
    // Fallback: originals
    images = list('gallery/images', /\.(jpe?g|png|webp|gif)$/i)
        .map((f) => ({
            src: '/' + enc(`gallery/images/${f}`),
            thumb: '/' + enc(`gallery/images/${f}`),
        }));
}

const videos = list('gallery/videos', /\.(mp4|mov|webm|m4v)$/i)
    .map((f) => ({ src: '/' + enc(`gallery/videos/${f}`) }));

const out = [
    '// Auto-generated by scripts/build-gallery-manifest.js — do not edit manually.',
    `export const GALLERY_IMAGES = ${JSON.stringify(images, null, 2)};`,
    '',
    `export const GALLERY_VIDEOS = ${JSON.stringify(videos, null, 2)};`,
    '',
].join('\n');

fs.writeFileSync(path.join(root, 'src/config/galleryManifest.js'), out);
console.log(`✅ Wrote ${images.length} images and ${videos.length} videos to manifest.`);
